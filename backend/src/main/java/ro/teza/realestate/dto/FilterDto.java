package ro.teza.realestate.dto;

import java.time.Instant;

public class FilterDto {

    private Long id;
    private String name;
    private String description;
    private FilterCriteriaDto criteria;
    private boolean active;
    private Instant createdAt;
    private Instant updatedAt;
    private String createdBy;
    private boolean subscribed; // Whether current user is subscribed to this filter

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public FilterCriteriaDto getCriteria() {
        return criteria;
    }

    public void setCriteria(FilterCriteriaDto criteria) {
        this.criteria = criteria;
    }

    public boolean isActive() {
        return active;
    }

    public void setActive(boolean active) {
        this.active = active;
    }

    public Instant getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Instant createdAt) {
        this.createdAt = createdAt;
    }

    public Instant getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(Instant updatedAt) {
        this.updatedAt = updatedAt;
    }

    public String getCreatedBy() {
        return createdBy;
    }

    public void setCreatedBy(String createdBy) {
        this.createdBy = createdBy;
    }

    public boolean isSubscribed() {
        return subscribed;
    }

    public void setSubscribed(boolean subscribed) {
        this.subscribed = subscribed;
    }
}
